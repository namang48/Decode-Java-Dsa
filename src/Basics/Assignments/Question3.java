package Basics.Assignments;

public class Question3 {
    public static void main(String[] args) {
        int x = 8, y = 7;
        System.out.println("INTEGER ARITHMATICS");
        System.out.println("Integer addition : " + (x + y));
        System.out.println("Integer subtraction : " + (x - y));
        System.out.println("Integer multiplication : " + (x * y));
        System.out.println("Integer division : " + (x / y));

        float p = 8.3f, q = 4.0f;
        System.out.println("\n\nFLOAT ARITHMATICS");
        System.out.println("Float addition : " + (p + q));
        System.out.println("Float subtraction : " + (p - q));
        System.out.println("Float multiplication : " + (p * q));
        System.out.println("Float division : " + (p / q));

        double a = 13.65, b = 1.35;
        System.out.println("\n\nDOUBLE ARITHMATICS");
        System.out.println("Double addition : " + (a + b));
        System.out.println("Double subtraction : " + (a - b));
        System.out.println("Double multiplication : " + (a * b));
        System.out.println("Double division : " + (a / b));


    }
}
